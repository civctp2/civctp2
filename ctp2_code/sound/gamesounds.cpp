sint32 gamesounds_GetGameSoundID(sint32 gamesound)
{
	sint32 index;

	if (gamesound < 0 || gamesound >= GAMESOUNDS_MAX) return 0;

	if ((index = g_theSoundDB->FindTypeIndex(g_gameSounds[gamesound].name)) != -1) {
		return index;
	} else {
		return 0;
	}
}

void gamesounds_WindowOpened(void)
{
	if (g_soundManager) {
		g_soundManager->AddSound(SOUNDTYPE_SFX, 0,
				gamesounds_GetGameSoundID(GAMESOUNDS_MAXWINDOW), 0, 0);
	}
}

void gamesounds_WindowClosed(void)
{
	if (g_soundManager) {
		g_soundManager->AddSound(SOUNDTYPE_SFX, 0,
				gamesounds_GetGameSoundID(GAMESOUNDS_MINWINDOW), 0, 0);
	}
}
