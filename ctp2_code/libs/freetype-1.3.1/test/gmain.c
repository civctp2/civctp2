  void  Goto_XY( int  x, int  y )
  {
    gcursor_x = x;
    gcursor_y = y;
  }


  void  Print_Str( char*  string )
  {
    if ( !string ) return;

    while ( *string )
    {
      switch ( *string )
      {
      case '\n':
        gcursor_x = 0;
        gcursor_y++;
        if ( gcursor_y > gwindow_height ) gcursor_y = 0;
        break;

      default:
        (*Print_8x8_Char)( gcursor_x, gcursor_y, *string );

        gcursor_x++;

        if ( gcursor_x >= gwindow_width )
        {
          gcursor_x = 0;
          gcursor_y++;

          if ( gcursor_y >= gwindow_height ) gcursor_y = 0;
        }
      }
      string++;
    }
  }


  void  Print_XY( int  x, int  y, char*  string )
  {
    Goto_XY( x, y );
    Print_Str( string );
  }









